package com.kirin.bean;

public class UserBean {
	
	
	private static final String id = "111111111";
	
	private String name;
	
	private int age;
	
	private int size;
	

	public static String getId() {
		return id;
	}


	public String getName() {
		return name;
	}


	public void setName(String name) {
		this.name = name;
	}


	public int getAge() {
		return age;
	}


	public void setAge(int age) {
		this.age = age;
	}


	public int getSize() {
		return size;
	}


	public void setSize(int size) {
		this.size = size;
	}
	


}
